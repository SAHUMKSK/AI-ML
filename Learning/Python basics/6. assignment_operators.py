# It is used to store or update values in variables.

a = 10 # Assignment
print(a) # 10

a += a # Addition assignment
print(a) # 10 + 10

a -= a # Subtraction assignment
print(a) # 20 - 20

a = 10
a *= a # Multiplication assignment
print(a) # 10 * 10

a /= a # Division assignment
print(a) # 100 / 100

a = 10
a //= a # Floor Division assignment
print(a)

a = 10
a %= a # Modulus assignment
print(a)

a = 10
a **= a # Exponentiation assignment
print(a) # 10 * 10